-- Index membership: real S&P 500 / ASX 200 / S&P/TSX 60 constituents.
--
-- The Run Analysis index baskets resolve to the actual constituents we cover
-- (membership INTERSECT universe), read at request time from this table so an
-- update goes live with NO redeploy. Maintained nightly by
-- analytics.cron.refresh_index_membership from official ETF holdings files
-- (SPY = US/State Street, IOZ = AU/iShares, XIU = CA/iShares). Server-only:
-- RLS enabled with NO policies, exactly like stocks / listings / ticker_requests
-- (see 20260614020000_enable_rls_lockdown + 20260620000000_request_a_ticker).
-- See docs/architecture.md §8 (Tier 4) and docs/data-contracts.md.

create table public.index_membership (
  index_id   text not null,                  -- 'sp500' | 'asx200' | 'tsx60'
  ticker     text not null,                  -- yfinance format: 'AAPL', 'BHP.AX', 'SHOP.TO'
  is_active  boolean not null default true,  -- false when a name drops out of the index (never deleted)
  updated_at timestamptz not null default now(),
  primary key (index_id, ticker),
  constraint valid_index_id check (index_id in ('sp500', 'asx200', 'tsx60'))
);

-- Partial index: the app reads only the active members of an index.
create index idx_index_membership_active on public.index_membership (index_id) where is_active;

-- Server-only: lock out anon/authenticated; app + cron use the service-role key
-- (which bypasses RLS). The "RLS enabled, no policy" advisor notice is intentional.
alter table public.index_membership enable row level security;

